#include <stdio.h>
#include <stdlib.h>


#include <math.h>

int main()
{
    /*
    ceil floor sqrt pow abs
    */
    printf("%f\n",ceil(4.01));  
    printf("%f\n",floor(4.9));
    printf("%f\n",sqrt(64));
    printf("%f\n",pow(2,6));
    printf("%d\n",abs(-15));

    return 0;
}
